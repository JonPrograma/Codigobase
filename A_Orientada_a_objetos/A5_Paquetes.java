    /*¿Qué es un paquete?
    
    Un paquete es un espacio de nombres que organiza un conjunto de clases e interfaces relacionadas. 
    Conceptualmente, puede pensar que los paquetes son similares a diferentes carpetas en su computadora.
    Puede mantener las páginas HTML en una carpeta, las imágenes en otra y los scripts o aplicaciones en otra. 
    Debido a que el software escrito en el lenguaje de programación Java puede estar compuesto 
    por cientos o miles de clases individuales, tiene sentido mantener las cosas organizadas colocando 
    clases relacionadas e interfaces en paquetes.

    La plataforma Java proporciona una enorme biblioteca de clases (un conjunto de paquetes) 
    adecuada para usar en sus propias aplicaciones. Esta biblioteca se conoce como "Interfaz 
    de programación de aplicaciones" o "API", para abreviar. Sus paquetes representan las tareas 
    más comúnmente asociadas con la programación de propósito general. Por ejemplo, un objeto String 
    contiene estado y comportamiento para cadenas de caracteres; un objeto de archivo permite a un programador 
    crear, eliminar, inspeccionar, comparar o modificar fácilmente un archivo en el sistema de archivos; un objeto 
    Socket permite la creación y uso de sockets de red; varios objetos GUI controlan botones y casillas 
    de verificación y cualquier otra cosa relacionada con las interfaces gráficas de usuario. 
    Hay literalmente miles de clases para elegir. Esto le permite a usted, el programador, 
    concentrarse en el diseño de su aplicación particular, en lugar de la infraestructura requerida 
    para que funcione.
    
*/
